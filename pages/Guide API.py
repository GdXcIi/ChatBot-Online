import streamlit as st

st.title("Comment récupérer sa clé API ?")

st.header("MistralAI")
st.markdown("Avant toute chose, commencez par vous rendre sur [La Plateforme Mistral](https://console.mistral.ai). Pui, une fois sur La Plateforme Mistral, cliquez sur API keys dans la barre de navigation à gauche et cliquez sur create a new key. Donnez un nom à votre clé API (par exemple `test`) puis copiez la. Collez la dans le champ réservé à cet effet sur la [page principal](http://localhost:8501/Accueil).")

st.header("OpenAI")
st.markdown("Tout d'abord pour créer votre clé API OpenAI (ce qui n'est malheureusement pas gratuit, bien que pas très cher), commencez par vous rendre sur [OpenAI Platform](https://platform.openai.com/api-keys) puis créez une nouvelle clé en n'oubliant pas de lui donner un nom puis de la copier. Une fois créée, collez la dans le champ réservé à cet effet sur la [page d'accueil](https://localhost:8501/Accueil).")
